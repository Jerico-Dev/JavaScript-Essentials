/** *******  *********  ********** ****** **/


Math.round(x) //returns the value of x rounded to its nearest integer:

Math.round(4.7);    
// returns 5
Math.round(4.4);    
// returns 4

/** *******  *********  ********** ****** **/


Math.pow(x, y) //returns the value of x to the power of y:

Math.pow(8, 2);      
// returns 64

/** *******  *********  ********** ****** **/

Math.sqrt(x) //returns the square root of x:
Math.sqrt(64);      
// returns 8

/** *******  *********  ********** ****** **/


Math.abs(-4.7); //returns the absolute (positive) value of x:
// returns 4.7


/** *******  *********  ********** ****** **/


Math.ceil(x) //returns the value of x rounded up to its nearest integer:
Math.ceil(4.4);     
// returns 5

/** *******  *********  ********** ****** **/

Math.floor(x) //returns the value of x rounded down to its nearest integer:
Math.floor(4.7);    
// returns 4

/** *******  *********  ********** ****** **/

Math.sin(x) //returns the sine (a value between -1 and 1) of the angle x (given in radians).

Math.sin(90 * Math.PI / 180);     
// returns 1 (the sine of 90 degrees)

/** *******  *********  ********** ****** **/

Math.cos(x) //returns the cosine (a value between -1 and 1) of the angle x (given in radians).
Math.cos(0 * Math.PI / 180);     
// returns 1 (the cos of 0 degrees)

/** *******  *********  ********** ****** **/

Math.min() , Math.max() //can be used to find the lowest or highest value in a list of arguments:

Math.min(0, 150, 30, 20, -8, -200);  
// returns -200

/** *******  *********  ********** ****** **/


Math.random() //returns a random number between 0 (inclusive), and 1 (exclusive):
Math.random();     
// returns a random number

